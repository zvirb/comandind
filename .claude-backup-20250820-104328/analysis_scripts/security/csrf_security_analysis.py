def main():
    """Generate and display CSRF security analysis report."""
    analyzer = CSRFSecurityAnalysis()
    report = analyzer.print_report()
    
    # Save detailed report to file
    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    filename = f"csrf_security_analysis_{timestamp}.json"
    
    with open(filename, 'w') as f:
        json.dump(report, f, indent=2, default=str)
    
    print(f"\nüíæ Detailed report saved to: {filename}")
    
    # Exit with error code if critical issues found
    if report['executive_summary']['critical_issues'] > 0:
        print("\nüö® CRITICAL SECURITY ISSUES DETECTED - IMMEDIATE ACTION REQUIRED!")
        return 1
    elif report['executive_summary']['high_issues'] > 0:
        print("\n‚ö†Ô∏è  HIGH PRIORITY SECURITY ISSUES DETECTED")
        return 1
    else:
        print("\n‚úÖ No critical security issues detected")
        return 0


if __name__ == "__main__":
    exit(main())